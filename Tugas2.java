package Pertemuan_4;

public class Tugas2 {
    public static void main(String[] args) {
        int k = 0;
        for (int i = 1; i <= 3; i++) {
            for (int j = 1; j <= 6; j++) {
                System.out.print(k + j + " ");
            }
            k++;
            System.out.println();
        }
    }
}
